import java.util.*;
public class Assignmeent1_Q1 {

	public static void main(String[] args) {
		
		Scanner sc = new Scanner(System.in);
		int num;
		System.out.println("Enter Number : ");
		num = sc.nextInt();
		
		System.out.println("Binary of "+num+" = "+Integer.toBinaryString(num));
		System.out.println("Octal of "+num+" = "+Integer.toOctalString(num));
		System.out.println("Binary of "+num+" = "+Integer.toHexString(num));


	}

}
